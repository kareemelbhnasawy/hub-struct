/**
 * Radius sizes from the design system
 *
 * Usage:
 * - Card radius: Radius.LG (16px)
 * - Button radius: Radius.MD (8px)
 */
export enum Radius {
  NONE = 0, // radius-none, Radius/radius-0
  XS = 2, // radius-xs, Radius/radius-2
  SM = 4, // radius-sm, Radius/radius-4
  MD = 8, // radius-md, Radius/radius-8
  LG = 16, // radius-lg, Radius/radius-16
  XL = 24, // radius-xl, Radius/radius-24
  FULL = 9999, // radius-full, Radius/radius-9999
}
